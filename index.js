// BOT START
create({
  session: "Chat-GPT",
  multidevice: true,
  ...browserArgs,
})
  .then((client) => start(client))
  .catch((error) => {
    console.log(error);
  });

const commands = (client, message) => {
  const botCommands = {
    davinci3: "/bot",
    dalle: "/img",
  };

  let firstWord = message.content.substring(0, message.content.indexOf(" "));

  switch (firstWord) {
    case botCommands.davinci3:
      const question = message.content.substring(message.content.indexOf(" "));
      try {
        getDavinciResponse(question, message.sender.displayName).then(
          (response) => {
            // Check if the message is from the bot or the user
            client.sendText(
              message.from === BOT_NUMBER ? message.to : message.from,
              response
            );
          }
        );
      } catch (error) {
        console.error(error);
      }
      break;

    case botCommands.dalle:
      const imgDescription = message.content.substring(
        message.content.indexOf(" ")
      );
      try {
        getDalleResponse(imgDescription, message).then((imgUrl) => {
          client
            .sendImage(
              message.from === BOT_NUMBER ? message.to : message.from,
              imgUrl,
              imgDescription,
              "Image generated by DALL-E ğŸ¤–"
            )
            .catch((e) => {
              client.sendText(
                message.from === BOT_NUMBER ? message.to : message.from,
                imgUrl
              );
            });
        });
      } catch (error) {
        console.error(error);
      }
      break;
  }
};

async function start(client) {
  client.onAnyMessage((message) => commands(client, message));
}
